public class SecurityFragment extends Fragment {
    Button button;
    VideoView videoView;

    // Your Video URL
    public SecurityFragment() {
        // Required empty public constructor
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_security, container, false);
        videoView = view.findViewById(R.id.videoview);
        String path = "android.resource://" + getActivity().getPackageName() + "/" + R.raw.videolocation;
        Uri uri = Uri.parse(path);

        // sets the resource from the
        // videoUrl to the videoView
        videoView.setVideoURI(uri);

        // creating object of
        // media controller class
        MediaController mediaController = new MediaController(getContext());

        // sets the anchor view
        // anchor view for the videoView
        mediaController.setAnchorView(videoView);

        // sets the media player to the videoView
        mediaController.setMediaPlayer(videoView);

        // sets the media controller to the videoView
        videoView.setMediaController(null);

        // starts the video
        videoView.start();
        button = view.findViewById(R.id.sharelocation);
        button.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent myIntent = new Intent(Intent.ACTION_SEND);
                myIntent.setType("text/plain");
                String body = "I'm in danger please track my location! Urgent I need help!" +
                        "\nSee my real time location on Maps:\nhttp ://maps . app . goo . gl";
                String sub = "Share Location";
                myIntent.putExtra(Intent.EXTRA_SUBJECT,sub);
                myIntent.putExtra(Intent.EXTRA_TEXT,body);
                startActivity(Intent.createChooser(myIntent, "Send via"));
            }
        });
        return view;
    }
}